numFiles = size(fileList,2);
for file=1:numFiles    
    
    page = ceil(file/plotsPerPage);
    figure(page);
    slot = mod(file-1,plotsPerPage)+1;
    if slot == 1
        myComment = comment;
    else 
        myComment = '';
    end
    myAxis = subplot(plotsPerPage,1,slot);
    analyzeRotationStrip(fileList{file},myComment,myAxis);
    
end

