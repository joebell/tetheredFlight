        end

        if (file == 1)
        	Xtotal{i} = data.wrappedX(partsList);
        else
        	Xtotal{i} = cat(2,Xtotal{i},data.wrappedX(partsList));
        end
        
        count = 0;
        for j = 1:size(PIwindow,1)
            count = count + nnz((data.wrappedX(partsList) > PIwindow(j,1)) ...
                & (data.wrappedX(partsList) <= PIwindow(j,2)));
        end
